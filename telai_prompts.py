# プロンプト構築関数
# ═══════════════════════════════════════════════════════════

_VALID_STAGES = frozenset(STAGE_PROMPTS.keys())


def build_telai_prompt(
    stage: str = "test_intro",
    customer_context: dict = None,
    tenant_context: dict = None,
    allow_final_close: bool = False,
    custom_overrides: dict = None,
) -> str:
    """
    テライの最終プロンプトを構築する。

    Args:
        stage: 現在の会話段階 (seeding/watering/fertilizing/harvesting/inbound/test_intro)
        customer_context: 顧客マスター情報 (MyMemGuard 連携前提、現状は枠のみ)
        tenant_context: 顧客企業情報 (会社名・業種など)
        allow_final_close: AI に最終契約権限を与えるか (harvesting 段階で使用)
        custom_overrides: 顧客企業のカスタム追加指示

    Returns:
        Gemini Live に渡す system_prompt 文字列
    """
    if stage not in _VALID_STAGES:
        stage = "test_intro"

    parts = [TELAI_BASE_PROMPT, STAGE_PROMPTS[stage]]

    if tenant_context:
        company = tenant_context.get("company_name", "")
        industry = tenant_context.get("industry", "")
        if company or industry:
            parts.append(f"""
【今回のお問い合わせ元企業情報】
会社名: {company}
業種: {industry}
""")

    if customer_context:
        past_calls = customer_context.get("past_calls_count", 0)
        interests = customer_context.get("interests", [])
        pain_points = customer_context.get("pain_points", [])
        parts.append(f"""
【お客様情報（過去の関係性）】
お名前: {customer_context.get("contact_name", "不明")}
会社名: {customer_context.get("company_name", "不明")}
過去の通話回数: {past_calls}
最終通話: {customer_context.get("last_call_at", "なし")}
過去の要約: {customer_context.get("past_call_summary", "なし")}
方言傾向: {customer_context.get("dialect_tendency", "standard")}
興味分野: {interests}
課題: {pain_points}
""")

    if not allow_final_close and stage == "harvesting":
        parts.append("""
【重要: 契約権限なし】
あなたには最終契約を締結する権限はありません。
具体的な契約・お見積もり・詳細打ち合わせは
必ず人間の担当者に引き継いでください。
""")

    if custom_overrides:
        extra = custom_overrides.get("extra_instructions", "")
        if extra:
            parts.append(f"""
【顧客企業からの追加指示】
{extra}
""")

    return "\n\n".join(parts)
