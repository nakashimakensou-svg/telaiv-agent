# ═══════════════════════════════════════════════════════════
# 顧客コンテキスト埋め込み関数
# ═══════════════════════════════════════════════════════════

def build_telai_prompt(scenario: str = "test_intro", customer_context: dict = None) -> str:
    """
    シナリオと顧客コンテキストから完全なシステムプロンプトを構築する。

    Args:
        scenario: 'test_intro' / 'sales' / 'inbound'
        customer_context: MyMemGuard連携時の顧客情報(将来用)

    Returns:
        完成したシステムプロンプト文字列
    """
    prompt = TELAI_BASE_PROMPT

    if scenario == "test_intro":
        prompt += TELAI_TEST_INTRO_ADDITION
    elif scenario == "sales":
        prompt += TELAI_SALES_ADDITION
    elif scenario == "inbound":
        prompt += TELAI_INBOUND_ADDITION

    if customer_context:
        prompt += f"""

# 【顧客コンテキスト】

このお電話の相手についての情報です。自然に活用してください:

- 名前: {customer_context.get('name', '不明')}
- 所在地: {customer_context.get('location', '不明')}
- タイムゾーン: {customer_context.get('timezone', '不明')}
- 現地時刻: {customer_context.get('current_local_time', '不明')}
- 業種: {customer_context.get('industry', '不明')}
- 過去通話: {customer_context.get('past_calls_count', 0)}回
- 直近の通話: {customer_context.get('last_call_summary', '初回')}
- 方言傾向: {customer_context.get('dialect_tendency', '標準語')}
"""

    return prompt
